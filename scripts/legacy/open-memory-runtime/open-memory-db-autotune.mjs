#!/usr/bin/env node

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = resolve(__dirname, "..");

const DEFAULT_AUDIT_PATH = resolve(REPO_ROOT, "output", "open-memory", "postgres-audit-latest.json");
const DEFAULT_ENV_PATH = resolve(REPO_ROOT, "output", "open-memory", "db-runtime-overrides.env");
const DEFAULT_JSON_PATH = resolve(REPO_ROOT, "output", "open-memory", "db-autotune-latest.json");

const flags = parseArgs(process.argv.slice(2));
const jsonOut = toBool(flags.json, false);
const strict = toBool(flags.strict, false);
const auditPath = resolve(REPO_ROOT, String(flags.audit || DEFAULT_AUDIT_PATH));
const envPath = resolve(REPO_ROOT, String(flags["out-env"] || DEFAULT_ENV_PATH));
const reportPath = resolve(REPO_ROOT, String(flags.out || DEFAULT_JSON_PATH));

if (toBool(flags.help, false)) {
  printHelp();
  process.exit(0);
}

if (!existsSync(auditPath)) {
  const payload = {
    ok: false,
    status: "fail",
    generatedAt: new Date().toISOString(),
    reason: "audit_missing",
    message: `Audit file not found: ${auditPath}`,
    action: "Run `npm run open-memory:ops:db:audit` first.",
  };
  emit(payload, jsonOut);
  process.exit(1);
}

let audit;
try {
  audit = JSON.parse(readFileSync(auditPath, "utf8"));
} catch (error) {
  const payload = {
    ok: false,
    status: "fail",
    generatedAt: new Date().toISOString(),
    reason: "audit_parse_failed",
    message: `Unable to parse audit file: ${error instanceof Error ? error.message : String(error)}`,
    action: "Re-run the DB audit to regenerate a valid JSON artifact.",
  };
  emit(payload, jsonOut);
  process.exit(1);
}

const summary = audit?.summary || {};
const diagnostics = audit?.diagnostics || {};
const connBreakdown = Array.isArray(diagnostics.connectionBreakdown) ? diagnostics.connectionBreakdown : [];
const inferredTotals = inferBreakdownTotals(connBreakdown);

const maxConnections = toInt(summary.maxConnections, 0);
const totalConnections = toInt(summary.totalConnections, inferredTotals.total);
const activeConnections = toInt(summary.activeConnections, inferredTotals.active);
const waitingConnections = toInt(summary.waitingConnections, 0);
const longestQuerySeconds = toInt(summary.longestQuerySeconds, 0);
const tempBytes = toInt(summary.tempBytes, 0);
const tooManyClientsSeen = Array.isArray(diagnostics.errors)
  && diagnostics.errors.some((entry) => /too many clients already/i.test(String(entry)));
const connectionRatio = maxConnections > 0 ? totalConnections / maxConnections : 0;

const pressureExtreme =
  tooManyClientsSeen
  || connectionRatio >= 0.9
  || totalConnections >= 100
  || activeConnections >= 95
  || longestQuerySeconds >= 900;

const pressureHigh =
  pressureExtreme
  || connectionRatio >= 0.75
  || totalConnections >= 80
  || activeConnections >= 70
  || waitingConnections > 0
  || longestQuerySeconds >= 300;

const profile = pressureExtreme ? "extreme" : pressureHigh ? "high" : "steady";

const overrides = computeOverrides({
  profile,
  maxConnections,
  totalConnections,
});

const envBody = [
  "# Auto-generated by scripts/open-memory-db-autotune.mjs",
  `# generatedAt=${new Date().toISOString()}`,
  `# profile=${profile}`,
  ...Object.entries(overrides).map(([key, value]) => `${key}=${value}`),
  "",
].join("\n");

mkdirSync(dirname(envPath), { recursive: true });
writeFileSync(envPath, envBody, "utf8");

const report = {
  ok: true,
  status: profile === "steady" ? "pass" : "warn",
  generatedAt: new Date().toISOString(),
  auditPath,
  envPath,
  reportPath,
  profile,
  inputs: {
    maxConnections,
    totalConnections,
    activeConnections,
    waitingConnections,
    longestQuerySeconds,
    tempBytes,
    connectionRatio: maxConnections > 0 ? round2(connectionRatio) : null,
    tooManyClientsSeen,
  },
  overrides,
  rationale: [
    profile === "extreme"
      ? "Detected severe DB pressure; aggressive throttling and tighter query budgets applied."
      : profile === "high"
        ? "Detected elevated DB pressure; moderate throttling and stronger timeouts applied."
        : "No severe pressure indicators; balanced safety/performance profile applied.",
    "Runtime overrides are consumed by open-memory ops stack/supervisor at startup.",
  ],
};

mkdirSync(dirname(reportPath), { recursive: true });
writeFileSync(reportPath, `${JSON.stringify(report, null, 2)}\n`, "utf8");

emit(report, jsonOut);

if (strict && report.status !== "pass") {
  process.exit(1);
}

