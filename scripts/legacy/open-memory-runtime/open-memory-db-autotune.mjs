function inferBreakdownTotals(rows) {
  let total = 0;
  let active = 0;
  for (const row of rows) {
    const count = toInt(row?.count, 0);
    total += count;
    if (String(row?.state || "").toLowerCase() === "active") {
      active += count;
    }
  }
  return { total, active };
}

function parseArgs(argv) {
  const out = {};
  for (let index = 0; index < argv.length; index += 1) {
    const token = String(argv[index] || "");
    if (!token.startsWith("--")) continue;
    const key = token.slice(2).trim().toLowerCase();
    const next = argv[index + 1];
    if (!next || String(next).startsWith("--")) {
      out[key] = "true";
      continue;
    }
    out[key] = String(next);
    index += 1;
  }
  return out;
}

function toBool(value, fallback) {
  const raw = String(value ?? "").trim().toLowerCase();
  if (!raw) return fallback;
  if (["1", "true", "yes", "on"].includes(raw)) return true;
  if (["0", "false", "no", "off"].includes(raw)) return false;
  return fallback;
}

function toInt(value, fallback) {
  const parsed = Number.parseInt(String(value ?? ""), 10);
  return Number.isFinite(parsed) ? parsed : fallback;
}

function round2(value) {
  const num = Number(value);
  if (!Number.isFinite(num)) return 0;
  return Math.round(num * 100) / 100;
}

function emit(payload, json) {
  if (json) {
    process.stdout.write(`${JSON.stringify(payload, null, 2)}\n`);
    return;
  }
  const lines = [];
  lines.push(`open-memory db autotune status: ${String(payload.status || "unknown").toUpperCase()}`);
  lines.push(`generatedAt: ${payload.generatedAt || "n/a"}`);
  if (payload.profile) lines.push(`profile: ${payload.profile}`);
  if (payload.message) lines.push(`message: ${payload.message}`);
  if (payload.action) lines.push(`action: ${payload.action}`);
  if (payload.envPath) lines.push(`envPath: ${payload.envPath}`);
  if (payload.reportPath) lines.push(`reportPath: ${payload.reportPath}`);
  if (payload.inputs) {
    lines.push("inputs:");
    for (const [key, value] of Object.entries(payload.inputs)) {
      lines.push(`  ${key}: ${value}`);
    }
  }
  if (payload.rationale && Array.isArray(payload.rationale)) {
    lines.push("rationale:");
    payload.rationale.forEach((entry, index) => lines.push(`  ${index + 1}. ${entry}`));
  }
  process.stdout.write(`${lines.join("\n")}\n`);
}

function printHelp() {
  process.stdout.write(
    [
      "Usage:",
      "  node ./scripts/open-memory-db-autotune.mjs [options]",
      "",
      "Options:",
      "  --audit <path>           Audit JSON input (default: output/open-memory/postgres-audit-latest.json)",
      "  --out-env <path>         Output env overrides file (default: output/open-memory/db-runtime-overrides.env)",
      "  --out <path>             Output JSON report (default: output/open-memory/db-autotune-latest.json)",
      "  --json true|false        Print JSON output (default: false)",
      "  --strict true|false      Exit non-zero when status != pass (default: false)",
      "  --help                   Show this help",
      "",
      "Examples:",
      "  npm run open-memory:ops:db:autotune",
      "  node ./scripts/open-memory-db-autotune.mjs --json true",
      "",
    ].join("\n"),
  );
}
